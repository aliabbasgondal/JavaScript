/*Exercise 18 Sorting an Array*/
seeing_the_world=['USA','UK','India','KSA','UAE','Turkia','Spain','New Zealand','Australia'];
console.log(seeing_the_world); // original Array
seeing_the_world_copy=seeing_the_world.slice(); //Copying Array into another array
seeing_the_world_copy.sort(); //sorting the copied array
console.log(seeing_the_world_copy); //print the copy array
console.log(seeing_the_world); //pring the original array
console.log(seeing_the_world_copy.reverse()); //print the copy array
console.log(seeing_the_world); //pring the original array
console.log(seeing_the_world.reverse()); //pring the original array
console.log(seeing_the_world.sort()); //pring the original array
console.log(seeing_the_world.sort().reverse()); //pring the original array
